#include  "BSP_Include.h"


/* ******************************************************************
* 函数名称： Timer0_Init()
* 入口参数：无
* 出口参数：无
* 函数功能：初始化定时器 0
****************************************************************** */
void Timer0_Init(void)
{
	ET0 = 0;		//初始化Timer0输出一个300KHZ时钟
	TR0 = 0;
	AUXR |=  0x80;	//Timer0 set as 1T mode
	AUXR2 |= 0x01;	//允许输出时钟
	TMOD = 0;		//Timer0 set as Timer, 16 bits Auto Reload.
	TH0 = (u8)(Timer0_Reload >> 8);
	TL0 = (u8)Timer0_Reload;
	TR0 = 1;
}
