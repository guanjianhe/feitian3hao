#include "FsBSP_AllLedFlash.h"
#include "FsBSP_Delay.h"

/* ****************************************************************************
 * 函数名称：LedGPIO_Init()
 * 入口参数：无
 * 出口参数：无
 * 函数功能：初始化所用端口
**************************************************************************** */
void LedGPIO_Init(void)
{
	P3M0 = 0x00;
    P3M1 = 0x00;
}
/* ****************************************************************************
 * 函数名称：AllLedFlash()
 * 入口参数：无
 * 出口参数：无
 * 函数功能：所有的LED闪烁
**************************************************************************** */
void AllLedFlash(void)
{
	P7 = 0xFF;
	LED9  = 1;
	LED10 = 1;
	LED11 = 1;
	LED12 = 1;
	DelayMS(1000);
	P7 = 0x00;
	LED9  = 0;
	LED10 = 0;
	LED11 = 0;
	LED12 = 0;
	DelayMS(1000);
}

