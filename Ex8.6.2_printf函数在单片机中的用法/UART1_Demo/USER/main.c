#include "FsBSP_Delay.h"
#include "FsBSP_Uart.h"
#include <stdio.h>

#define uChar8 unsigned char
#define uInt16 unsigned int

/* ***************************************************** */
// 函数名称：Arithmetic()
// 函数功能：串口演示算术运算
// 入口参数：第一第二操作数(a,b)
// 出口参数：无
/* ***************************************************** */
void Arithmetic(uInt16 a,uInt16 b)
{
	float fTemp;
	uInt16 uTemp;
	TI = 1;
	printf("\r  a的值为：%d...\n",a);
	TI = 1;
	printf("\r  b的值为：%d...\n",b);
	uTemp = a + b;
	TI = 1;
	printf("\r a + b = %d   \r\n",uTemp);
	uTemp = a - b;
	TI = 1;
	printf("\r a - b = %d   \r\n",uTemp);
	uTemp = a * b;
	TI = 1;
	printf("\r a × b = %d  \r\n",uTemp);
	fTemp = (float)a / b;
	TI = 1;
	printf("\r a ÷ b = %f  \r\n",fTemp);
} 
/* ***************************************************** */
// 函数名称：main()
// 函数功能：串口初始化后发广告语再做算术演示
// 入口参数：无
// 出口参数：无
/* ***************************************************** */
void main(void)
{
	UART1_Init();
	DelayMS(100);
	UART_SendString("\r /* =========================================== */ \r\n" );
	UART_SendString("\r 欢迎使用w天叁（FSST15）开发板.............. \r\n" );
	UART_SendString("\r /* =========================================== */ \r\n" );
	
	Arithmetic(13,7);

	while(1);
}

