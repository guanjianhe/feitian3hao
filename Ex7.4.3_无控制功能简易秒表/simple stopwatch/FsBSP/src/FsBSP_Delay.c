#include "FsBSP_Delay.h"

/* ****************************************************************************
 * 函数名称：DelayMS()
 * 入口参数：ms，要延时的ms数
 * 出口参数：无
 * 函数功能：不精确延时
**************************************************************************** */
void DelayMS(unsigned int ms)
{
     unsigned int i;
	 do{
	      i = MAIN_Fosc / 13000;
		  while(--i);
     }while(--ms);
}

