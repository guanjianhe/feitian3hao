#include "FsBSP_Key.h"
#include "STC15.h"

/* ****************************************************************************
 * 函数名称：LedGPIO_Init()
 * 入口参数：无
 * 出口参数：无
 * 函数功能：设置P4^0为低电平，则k2，k3，k4，k5则可以组成四个独立按键
**************************************************************************** */
void SingleKey_Init(void)
{
	KEY0 = 0;
}
/* ****************************************************************************
 * 函数名称：KeyScan(void)
 * 入口参数：无
 * 出口参数：无
 * 函数功能：通过操作K2，K3对输出的PWM的占空比进行调整
**************************************************************************** */
void KeyScan(void)
{
	if(!KeyInc)
	{
		DelayMS(10);
		if(!KeyInc)
		{
			ucCount++;
			uCount = 0;
			if(6 == ucCount)
				ucCount = 5;
			while(!KeyInc);	
		}
	}

	if(!KeyDec)
	{
		DelayMS(10);
		if(!KeyDec)
		{
			ucCount--;
			uCount = 0;
			if(0 == ucCount)
				ucCount = 1;
			while(!KeyDec);	
		}
	}
}


