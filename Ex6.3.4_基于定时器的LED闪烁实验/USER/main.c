#include "FsBSP_Timer.h"	// 包含定时器的头文件

sbit LED11 = P3^6;			// LED11的位定义

void main(void)
{
	unsigned char uiCounter = 0;
	
	Timer0_Init();			// 调用初始化函数，初始化定时器0
	
	while(1)
	{
		if(TF0 == 1)			// 检查定时器是否溢出
		{
			TF0 = 0;			// 有溢出时要记得清标志位哦
			uiCounter++;		// 记录溢出次数
			if(100 == uiCounter)// 一次10ms，那100次呢？
			{
				uiCounter = 0;
				LED11 = ~ LED11;// LED11取反，实现闪烁现象
			}
		}		
	}
}

