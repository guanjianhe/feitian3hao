#include "FsBSP_HC595.h"

void main(void)
{
	P6 = 0x00;				// 使其8位数码管全部有效（位选数据）
	HC595_WrOneByte(0x5b);	// 给595送0x5b，最后数据对应于数码管上（段选数据）
	while(1);
}

